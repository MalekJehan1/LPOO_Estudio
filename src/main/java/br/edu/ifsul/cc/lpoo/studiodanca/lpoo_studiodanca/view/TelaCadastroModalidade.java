    public void preencheListaProf() {
        jpa = new PersistenciaJPA();

        List<Professor> listProf = null;

        try {
            jpa.conexaoAberta();
            listProf = jpa.getProfessores();
            for (Professor p : listProf) {
                jCBoxProfessor.addItem(p);
            }
            jpa.fecharConexao();
        } catch (Exception e) {
            System.out.println("Erro ao buscar professores: \n" + e.getMessage());
        }

        if (modalidade != null) {
            txtDescricao.setText(modalidade.getDescricao());

            if (modalidade.getProfessor() != null) {
                for (Professor p : listProf) {
                    if (p.getId() == modalidade.getProfessor().getId()) {
                        jCBoxProfessor.setSelectedItem(p);
                    }
                }
            } else {
                jCBoxProfessor.setSelectedIndex(-1);
            }
        }
    }

