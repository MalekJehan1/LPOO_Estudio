    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:

        if (modalidade == null) {
            try {
                modalidade = new Modalidade();
                modalidade.setDescricao(txtDescricao.getText());
                modalidade.setProfessor((Professor) jCBoxProfessor.getSelectedItem());

                jpa = new PersistenciaJPA();
                jpa.conexaoAberta();
                jpa.persist(modalidade);
                jpa.fecharConexao();
                dispose();
            } catch (Exception ex) {
                System.out.println("Erro ao inserir nova modalidade");
            }
        } else {

            jpa = new PersistenciaJPA();

            try {
                Modalidade m = new Modalidade();
                m = modalidade;
                m = (Modalidade) jpa.find(Modalidade.class, m.getId());
                m.setDescricao(txtDescricao.getText());
                m.setProfessor((Professor) jCBoxProfessor.getSelectedItem());
                jpa.conexaoAberta();
                jpa.persist(m);
                jpa.fecharConexao();
                dispose();
            } catch (Exception e) {
                System.out.println("Erro" + e.getMessage());
            }

        }

    }//GEN-LAST:event_btnSalvarActionPerformed

